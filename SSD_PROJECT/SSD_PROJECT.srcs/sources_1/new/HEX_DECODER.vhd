LIBRARY ieee;
USE ieee.std_logic_1164.all;

ENTITY HEX_DECODER IS
  PORT (
	NUM: IN STD_LOGIC_VECTOR (3 DOWNTO 0);
	NUM_SSD: OUT STD_LOGIC_VECTOR (7 DOWNTO 0)
--	ENABLE: IN STD_LOGIC
    );
END HEX_DECODER;


ARCHITECTURE TypeArchitecture OF HEX_DECODER IS

BEGIN
PROCESS(NUM)
BEGIN

	CASE NUM IS
	WHEN "0000" =>	NUM_SSD <= "00000011";
	WHEN "0001" => NUM_SSD <= "10011111";
	WHEN "0010" => NUM_SSD <= "00100101";
	WHEN "0011" => NUM_SSD <= "00001101";
	WHEN "0100" => NUM_SSD <= "10011001";
	WHEN "0101" => NUM_SSD <= "01001001";
	WHEN "0110" => NUM_SSD <= "01000001";
	WHEN "0111" => NUM_SSD <= "00011111";
	WHEN "1000" => NUM_SSD <= "00000001";
	WHEN "1001" => NUM_SSD <= "00001001";
	WHEN "1010" => NUM_SSD <= "00010001";
	WHEN "1011" => NUM_SSD <= "11000001";
	WHEN "1100" => NUM_SSD <= "01100011";
	WHEN "1101" => NUM_SSD <= "10000101";
	WHEN "1110" => NUM_SSD <= "01100001";
	WHEN "1111" => NUM_SSD <= "01110001";
	END CASE;
END PROCESS;

END TypeArchitecture;
